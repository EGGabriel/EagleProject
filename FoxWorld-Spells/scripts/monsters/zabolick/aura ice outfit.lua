local function AttackDown(param)
	if isCreature(param.cid) == FALSE then -- interrompe a magia se cid deslogar ou morrer
		return TRUE
	end
	doSendDistanceShoot({x = param.pos.x - 7, y = param.pos.y - 5, z = param.pos.z}, param.pos, CONST_ANI_ICE)
	doCombat(param.cid, param.combat, positionToVariant(param.pos))
end

function onTargetTile(cid, pos)
	if (math.random(1,100) <= 20) then -- 5% de chance de cair a chuva no SQM da área
		addEvent(AttackDown, math.random(0, 1500), {cid = cid, combat = combat, pos = pos})
	end
end
setCombatCallback(acombat, CALLBACK_PARAM_TARGETTILE, "onTargetTile")

function onCastSpell(cid, var)
	doCombat(cid, acombat, var)
	return TRUE
end
